add=num1+num2
pow_of_numbers=pow(num1,num2)
print(f"Max of Two Numbers {max_number}")
print(f"Multiplication of Two Numbers {mul}")
print(f"Subtraction of  Two Numbers {sub_number}")
print(f"Addition of  Two Numbers {add}")
print(f"Division of  Two Numbers {div}")
print(f"Power of  Two Numbers {pow_of_numbers}")
